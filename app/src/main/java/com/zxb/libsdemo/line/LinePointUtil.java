    public static void handleLinePointList(ArrayList<LinePoint> linePointList) {
        for (int i = 0; i < linePointList.size(); i++) {
            if (i == 0 && linePointList.size() > 1) {
                // 第一个点
                LinePoint currentPoint = linePointList.get(i);
                currentPoint.setLeftPoint(-1f, -1f);
                LinePoint nextPoint = linePointList.get(i + 1);
                currentPoint.setRightPoint(
                        (currentPoint.getCenterFloatX() + nextPoint.getCenterFloatX()) / 2f,
                        (currentPoint.getCenterFloatY() + nextPoint.getCenterFloatY()) / 2f);
            } else if (i == linePointList.size() - 1 && linePointList.size() > 1) {
                // 最后一个点
                LinePoint currentPoint = linePointList.get(i);
                currentPoint.setRightPoint(-1f, -1f);
                LinePoint prevPoint = linePointList.get(i - 1);
                currentPoint.setLeftPoint(
                        (currentPoint.getCenterFloatX() + prevPoint.getCenterFloatX()) / 2f,
                        (currentPoint.getCenterFloatY() + prevPoint.getCenterFloatY()) / 2f);
            } else {
                // 既不是第一个，也不是最后一个
                LinePoint currentPoint = linePointList.get(i);
                LinePoint prevPoint = linePointList.get(i - 1);
                LinePoint nextPoint = linePointList.get(i + 1);
                currentPoint.setRightPoint(
                        (currentPoint.getCenterFloatX() + nextPoint.getCenterFloatX()) / 2f,
                        (currentPoint.getCenterFloatY() + nextPoint.getCenterFloatY()) / 2f);
                currentPoint.setLeftPoint(
                        (currentPoint.getCenterFloatX() + prevPoint.getCenterFloatX()) / 2f,
                        (currentPoint.getCenterFloatY() + prevPoint.getCenterFloatY()) / 2f);
            }
        }
    }
